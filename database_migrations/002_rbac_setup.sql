-- ========= Section 2: Helper Functions for RLS =========

CREATE OR REPLACE FUNCTION public.get_user_role_id()
RETURNS BIGINT AS $$
DECLARE
  user_role_id BIGINT;
BEGIN
  SELECT role_id INTO user_role_id
  FROM public.staff_members
  WHERE id = auth.uid()
  LIMIT 1;
  RETURN user_role_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.user_has_permission(p_permission_name TEXT)
RETURNS BOOLEAN AS $$
DECLARE
  user_role_id BIGINT;
  has_perm BOOLEAN;
BEGIN
  user_role_id := public.get_user_role_id();
  IF user_role_id IS NULL THEN
    RETURN FALSE;
  END IF;

  SELECT EXISTS (
    SELECT 1
    FROM public.role_permissions rp
    JOIN public.permissions p ON rp.permission_id = p.id
    WHERE rp.role_id = user_role_id AND p.name = p_permission_name
  ) INTO has_perm;
  
  RETURN has_perm;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.get_user_branch_id()
RETURNS UUID AS $$
DECLARE
  user_branch_id UUID;
BEGIN
  -- Note: Supabase stores branch_id as text, so we cast it to UUID for comparison
  -- with the branches table's id (which is UUID type).
  SELECT branch_id::UUID INTO user_branch_id
  FROM public.staff_members
  WHERE id = auth.uid()
  LIMIT 1;
  RETURN user_branch_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to update role permissions transactionally
CREATE OR REPLACE FUNCTION public.update_role_permissions(p_role_id bigint, p_permission_ids bigint[])
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path = public
AS $function$
BEGIN
    DELETE FROM public.role_permissions WHERE role_id = p_role_id;
    IF p_permission_ids IS NOT NULL AND array_length(p_permission_ids, 1) > 0 THEN
        INSERT INTO public.role_permissions (role_id, permission_id)
        SELECT p_role_id, permission_id
        FROM unnest(p_permission_ids) AS t(permission_id);
    END IF;
END;
$function$;


-- ========= Section 3: RLS Policies for All Tables =========

-- --- branches Table ---
ALTER TABLE public.branches ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Allow read access based on permissions" ON public.branches;
CREATE POLICY "Allow read access based on permissions" ON public.branches FOR SELECT USING (user_has_permission('branches_view'));
DROP POLICY IF EXISTS "Allow write access based on permissions" ON public.branches;
CREATE POLICY "Allow write access based on permissions" ON public.branches FOR ALL USING (
  (user_has_permission('branches_create') AND TG_OP = 'INSERT') OR
  (user_has_permission('branches_update') AND TG_OP = 'UPDATE') OR
  (user_has_permission('branches_delete') AND TG_OP = 'DELETE')
) WITH CHECK (
  (user_has_permission('branches_create') AND TG_OP = 'INSERT') OR
  (user_has_permission('branches_update') AND TG_OP = 'UPDATE') OR
  (user_has_permission('branches_delete') AND TG_OP = 'DELETE')
);

